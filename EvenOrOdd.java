package BasicProgram.softedge;

public class EvenOrOdd {

	public static void EvenOdd(int num)
	{
		if(num%2!=0)
		{
			System.out.println("odd number");
		}
		else
		{
			System.out.println("even number");
		}
	}
	public static void main(String[] args) {
		
		EvenOdd(20);
		
		
	}
}
